export const getCountryColors = (): { [key: string]: string } =>
  countryCodes
    .map((code, index) => ({ [code]: predefinedCountryColors[code] || palette[index % palette.length] }))
    .reduce((accumulator, currentItem) => ({ ...accumulator, ...currentItem }), {})
